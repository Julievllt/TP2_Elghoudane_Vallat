package model.emotion;


/**
 * Enum représentant les différents types d'émotions d'un Tamagotchi.
 * Chaque valeur de cet énumère correspond à une émotion spécifique que le Tamagotchi peut éprouver.
 */
public enum EmotionType {
	/** Joie : Représente l'émotion de bonheur ou de plaisir. */
	JOY, 

	/** Tristesse : Représente l'émotion de tristesse ou de désespoir. */
	SADNESS, 

	/** Colère : Représente l'émotion de frustration ou de rage. */
	ANGER, 

	/** Peur : Représente l'émotion de crainte ou d'anxiété. */
	FEAR
}
